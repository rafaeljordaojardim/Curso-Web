/*
    3 palavras reservadas
    var = declaração de variavel
    let = declaração de variavel
    const = declaração de constante
*/

/*
    atribuição do numero 3 a variavel 'a'
    Sempre colocar nomes faceis e legiveis para outras pessoas
    permite redeclarar a variavel
*/
var a = 3; 

/*
    forma mais moderna de criar variavel
    let não permite redeclaração
*/
let b = 4;
console.log(a, b);

a = 300;
b = 400;

console.log(a, b);

const c = 5;
//c = 50
